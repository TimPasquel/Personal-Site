<h2 id="general">General</h2>
<ul>
<li><p><strong>Operating System</strong></p>
<p>I use a debian version of Linux called <a href="https://www.linuxmint.com/">Mint Linux</a>. Mint Linux is a free operating system that has a fairly user friendly interface that gives off a similar vibe to Windows, but with more user control (which is a good thing). Its a light operating system that uses less system resources than pretty much any <span class="underline">paid</span> software in todays day and age. In conclusion, I highly recommend any intelligent computer user to switch to it as you will finally be released from the chains of todays terrible operating systems.</p></li>
<li><p><strong>Text Processing</strong></p>
<p>I use <a href="https://www.vim.org/">Vim</a> for my text processing because it is flexible and customizable while also working well with pretty much everything I need to do. Other software like emacs and nano are either too complicated for what I need it to do or have too much stuff on the screen, hindering my productivity.</p></li>
<li><p><strong>Web Browsing</strong></p>
<p>I use <a href="https://www.mozilla.org/en-US/firefox/linux/">Firefox</a> for my web browsing as it is one of the more light weight browsers that offers “slightly” (Note: “slightly”) better security. It also gives me the option to download third party extensions to make my web browsing experience better.</p></li>
<li><p><strong>Writing Documents</strong></p>
<p>I use <a href="https://www.latex-project.org/">LaTeX</a> mainly for creating my documents as it is <span class="underline">extreamly</span> flexible with document creation, as well as can be converted into HTML using pandoc with a single, simple command (Thats how I wrote this page by the way).</p></li>
<li><p><strong>Misc. Office Programs</strong></p>
<p>I know that there are better ways to do presentations and data analytics, but that requires further research that I still need to do. In the meantime I am using <a href="https://www.libreoffice.org/">Libreoffice</a> Calc, and Presenations as that is a much lighter software than both Microsoft Office and Office365 Plus, did I mention that it is <span class="underline">free</span> and does the exact same thing as the programs I just mentioned. Crazy!</p></li>
<li><p><strong>Messaging</strong></p>
<p>I use <a href="https://www.signal.org/">Signal</a> for my messaging service as it has full end to end encryption between devices. Especially now, I would rather that people do not have access to my data while it is being transmitted. There is also a note to self feature that is encypted that syncs between devices that are connected to your number. Furthermore, it is able to send larger files compared to something like SMS.</p></li>
</ul>
